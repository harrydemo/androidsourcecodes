package com.appspot.hexagon.dbo;

import android.provider.BaseColumns;

/**
 * @author Admin
 *
 */
public class WorldMap{
	public interface Constants extends BaseColumns {
		// table:hexagon.WorldMap
		public static final String TABLE = "worldmap";
		// columns
		public static final String _ID = "map_id";
		public static final String NAME = "map_name";
		public static final String DESC = "map_desc";
		public static final String WIDTH = "map_width";
		public static final String HEIGHT = "map_height";
	}
	
	private int id = -1;
	private String name = null;
	private String desc = null;
	private int width = 0;
	private int height = 0;	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}

}
