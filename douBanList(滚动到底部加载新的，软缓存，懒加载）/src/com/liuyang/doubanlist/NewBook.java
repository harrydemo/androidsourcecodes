package com.liuyang.doubanlist;

public class NewBook {
	private String bookName;
	private String bookMessage;
	private String bookSynopsis;
	private String bookPicturePath;
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public String getBookMessage() {
		return bookMessage;
	}
	public void setBookMessage(String bookMessage) {
		this.bookMessage = bookMessage;
	}
	public String getBookSynopsis() {
		return bookSynopsis;
	}
	public void setBookSynopsis(String bookSynopsis) {
		this.bookSynopsis = bookSynopsis;
	}
	public String getBookPicturePath() {
		return bookPicturePath;
	}
	public void setBookPicturePath(String bookPicturePath) {
		this.bookPicturePath = bookPicturePath;
	}
	
}
