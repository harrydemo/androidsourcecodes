package com.gw.android.boom.pojo;

public class M0 {
	private int x;
	private int y;

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public M0() {
		super();
	}

	public M0(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

}
